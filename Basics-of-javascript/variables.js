// variables.js

// Using var (not recommended in modern JS)
var a = 5;

// Using let
let b = 10;

// Using const herw we cant reassign to value it again

const c = 15;

// Variable reassignment (allowed with let, not allowed with const)
b = 20;

// Output values
console.log(a); // 5
console.log(b); // 20
console.log(c); // 15
